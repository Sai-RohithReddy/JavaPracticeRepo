//Swapping two numbers.

package java_assignment;

public class Assignment_3 {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c;
		System.out.println(".........before swap.........");
		System.out.println("a="+a+" b="+b);
		
		c = a;
		a = b;
		b = c;
		System.out.println(".........after swap.........");
		System.out.println("a="+a+" b="+b);

	}

}
