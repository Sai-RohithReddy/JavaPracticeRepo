package Loops;

public class ForLoop {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++)//(initialization 1*; condition 2*; increment/decrement 4*)
		{
			System.out.println(i);//3*
		}
		System.out.println("*************");
		
		for(int a = 10; a >= 0;a--)
		{
			System.out.println(a);
		}

	}

}
